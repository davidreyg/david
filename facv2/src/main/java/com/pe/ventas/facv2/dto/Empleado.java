package com.pe.ventas.facv2.dto;

public class Empleado {
	private String id_empleado;
	private String nom_emp;
	private String apel_emp;
	private String tel_emp;
	private String dir_emp;
	private String sex_emp;
	private String fnac_emp;
	private String dni_emp;
	private String ema_emp;

	public String getId_empleado() {
		return id_empleado;
	}

	public void setId_empleado(String id_empleado) {
		this.id_empleado = id_empleado;
	}

	public String getNom_emp() {
		return nom_emp;
	}

	public void setNom_emp(String nom_emp) {
		this.nom_emp = nom_emp;
	}

	public String getApel_emp() {
		return apel_emp;
	}

	public void setApel_emp(String apel_emp) {
		this.apel_emp = apel_emp;
	}

	public String getTel_emp() {
		return tel_emp;
	}

	public void setTel_emp(String tel_emp) {
		this.tel_emp = tel_emp;
	}

	public String getDir_emp() {
		return dir_emp;
	}

	public void setDir_emp(String dir_emp) {
		this.dir_emp = dir_emp;
	}

	public String getSex_emp() {
		return sex_emp;
	}

	public void setSex_emp(String sex_emp) {
		this.sex_emp = sex_emp;
	}

	public String getFnac_emp() {
		return fnac_emp;
	}

	public void setFnac_emp(String fnac_emp) {
		this.fnac_emp = fnac_emp;
	}

	public String getDni_emp() {
		return dni_emp;
	}

	public void setDni_emp(String dni_emp) {
		this.dni_emp = dni_emp;
	}

	public String getEma_emp() {
		return ema_emp;
	}

	public void setEma_emp(String ema_emp) {
		this.ema_emp = ema_emp;
	}

}
